package com.pobitecoding.project.vo;

public class BookVO {
    
    private String name;
    private int id;
    private String gender;
    
    public String getName() {
        return name;
    }
    public int getId() {
        return id;
    }
    public String getGender() {
        return gender;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setId(int id) {
        this.id = id;
    }
    public void setGender(String gender) {
        this.gender = gender;
    }
    
    
}