package com.pobitecoding.exam01;

/**
 * 화면에 다음과 같은 결과를 표시하는 소스 코드를 작성 해 주세요.
 * 
 * @author pobite
 *
 */
public class Exam1_2 {

    public static void main(String[] args) {


        // 1. 소스코드 안에 3을 변수 a에, 5를 변수 b에 대입합니다.
        int a = 3;
        int b = 5;

        // 2. 그곱셈의결과를변수 c 에대입합니다.
        int c = a * b;

        // 3. 변수 a, b, c를 이용하여 다음과 같이 화면에 출력합니다.
        System.out.println("가로 " + a + ", 세로 " + b + "의 직사각형의 면적은 " + c);

        // 출력 예) 가로3, 세로 5의 직사각형의 면적은 15
    }
}
