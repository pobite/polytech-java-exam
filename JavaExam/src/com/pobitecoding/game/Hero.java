package com.pobitecoding.game;

public class Hero {
    
    /*
     * member variables
     */
    private String name;
    private int hp;

    
    /*
     * getter/setter
     */
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public int getHp() {
        return hp;
    }
    
    public void setHp(int hp) {
        this.hp = hp;
    }
    
}